package beans;

public class Category {

    String name;
    int noProducts;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNoProducts() {
        return noProducts;
    }

    public void setNoProducts(int noProducts) {
        this.noProducts = noProducts;
    }

}
